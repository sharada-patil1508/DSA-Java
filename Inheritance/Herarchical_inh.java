package Inheritance;

public class Herarchical_inh {
	
	public void a()
	{
		System.out.println("class a");
	}

}
class b extends Herarchical_inh
{
	public void aa()
	{
		System.out.println("class b");
	}
}
class c extends Herarchical_inh
{
	public void aaa()
	{
		System.out.println("class c");
	}
}
